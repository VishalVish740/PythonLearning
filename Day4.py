# Day 9- Type Casting --> Conversion of one datatype into other datatype
a = "Vishal"
b = "Vishwakarma"
print(a + b)

# Explicit Type Casting --> Manually as per requirement
x = "5"
y = "6"
print(int(x) + int(y))

# Implicit Type Casting --> Done by the computer or predefined
c = 3.5
d = 5
print(c + d)

